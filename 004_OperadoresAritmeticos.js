let n1 = 2;
let n2 = 8;
console.log(n1 + n2);    //10 adição
console.log('1' + '8');  //'28' contatenação
console.log(n1 - n2);    //-6 subtração
console.log(n1 * n2);    //16 multiplicação
console.log(n1 / n2);    //0.25 divisão
console.log(n1 ** n2);   //256 potência
console.log(n1 % n2);    //2 resto da divisão
let incremento = 1;
incremento++; // + 2
console.log(incremento); //2
decremento--;
console.log(decremento); //0
